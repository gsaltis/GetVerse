/*****************************************************************************!
 * Global Headers
 *****************************************************************************/
#include <QtCore>
#include <QtGui>
#include <QWidget>

/*****************************************************************************!
 * Local Headers
 *****************************************************************************/
#include "VerseInfo.h"

/*****************************************************************************!
 * Function : VerseInfo
 *****************************************************************************/
VerseInfo::VerseInfo
() : QWidget()
{
  Initialize();
}

/*****************************************************************************!
 * Function : VerseInfo
 *****************************************************************************/
VerseInfo::VerseInfo
(int InBook, int InChapter, int InVerse, QString InText) : QWidget()
{
  Initialize();
  Book          = InBook;
  Chapter       = InChapter;
  Verse         = InVerse;
  Text          = InText;
}

/*****************************************************************************!
 * Function : ~VerseInfo
 *****************************************************************************/
VerseInfo::~VerseInfo
()
{
}

/*****************************************************************************!
 * Function : GetBook
 *****************************************************************************/
int
VerseInfo::GetBook(void)
{
  return Book;  
}

/*****************************************************************************!
 * Function : SetBook
 *****************************************************************************/
void
VerseInfo::SetBook
(int InBook)
{
  Book = InBook;  
}

/*****************************************************************************!
 * Function : GetChapter
 *****************************************************************************/
int
VerseInfo::GetChapter(void)
{
  return Chapter;  
}

/*****************************************************************************!
 * Function : SetChapter
 *****************************************************************************/
void
VerseInfo::SetChapter
(int InChapter)
{
  Chapter = InChapter;  
}

/*****************************************************************************!
 * Function : GetVerse
 *****************************************************************************/
int
VerseInfo::GetVerse(void)
{
  return Verse;  
}

/*****************************************************************************!
 * Function : SetVerse
 *****************************************************************************/
void
VerseInfo::SetVerse
(int InVerse)
{
  Verse = InVerse;  
}

/*****************************************************************************!
 * Function : GetText
 *****************************************************************************/
QString
VerseInfo::GetText(void)
{
  return Text;  
}

/*****************************************************************************!
 * Function : SetText
 *****************************************************************************/
void
VerseInfo::SetText
(QString InText)
{
  Text = InText;  
}

/*****************************************************************************!
 * Function : Initialize
 *****************************************************************************/
void
VerseInfo::Initialize(void)
{
  Book          = 0;
  Chapter       = 0;
  Verse         = 0;
  Text          = QString();
}
